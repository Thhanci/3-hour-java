/*

============================================================
this 关键字详解
============================================================

【this 是什么？】
  this 指代当前实例（当前对象本身）。

【this 的两种用法】
  1. this.xxx  → 指代当前对象的字段或方法
  2. this()    → 调用当前类的另一个构造方法

【this() 是什么？】
  this() 表示调用当前类的另一个构造方法。
  例如：在 Person(String name) 中调用 this()，就是调用 Person() 无参构造。

【为什么要用 this()？】
  1. 代码复用：把多个构造方法中重复的初始化代码抽取到一处，避免重复写。
  2. 强制初始化顺序：保证先执行公共的初始化逻辑，再执行各自的逻辑。

  例如：无参构造里有 "实例化对象 + 打印" 的逻辑，
  带参构造里不用再重新写一遍，直接 this() 调用就行。

【为什么 this() 只能放开头？】
  Java 语言规定：用 this() 调用另一个构造方法时，
  必须写在构造方法的第一行（第一条语句）。

  原因：
  1. 保证初始化顺序正确 —— 必须先完成对象的通用初始化，
     才能继续做当前构造方法特有的初始化。
  2. 防止在初始化完成之前，就用了尚未初始化的部分。

  错误示例：
  Person(String name) {
      this.name = name;    // ❌ 先赋值
      this();              // ❌ 再调 this()，会报错
  }

  正确写法：
  Person(String name) {
      this();              // ✅ 必须在第一行
      this.name = name;    // ✅ 然后再做自己的事情
  }

【this() vs this 的区别】
  this()   → 调用当前类的另一个构造方法（必须在第一行）
  this.xxx → 指代当前对象的字段或方法（可以在任意位置）

【注意事项】
  1. 在带参构造中用 this() 调用无参构造时，
     无参构造里的代码会先执行完，再回到带参构造继续执行。
  2. 不能在同一个构造方法里调用两次 this()。
  3. 不能用类名加括号的方式调用构造方法（如 person()），
     那是错误写法，构造方法的调用只能用 this() 或 super()。
  4. 每次创建对象时，传入的参数类型必须匹配某一个已定义的构造方法。
============================================================

*/