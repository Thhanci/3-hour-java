package l7;

public class Person {

    String name;
    Integer age;

    Person() {
        // 1. 实例化一个对象

        // 2. 根据程序员的想法进行初始化，例如为当前对象赋初始值等
        // 做了很多事情
        System.out.println("我正在构造");
    }

    // Tip:fn+alt + insert
    Person(String name) {
        this();      //Tip: 用this();而不是person();，在这里用person（）；等于在person里再构造一次person（），因为 this在这里的意思是直接调用写好的主构造函数并省略重复构造；每次创建对象的时候，你所用的参数类型必须和某一个写好的构造函数一致
        this.name = name;

        //在这个this()里面，1实例化一个对象，2执行this里面程序员写的方法体
        //this();     //不能在这里用this();，因为在这里用的话，会吧this（）里的东西执行一遍再调用，所以this得放开头
    }




    public Person(String name, Integer age) {
        this.name = name;
        this.age = age;
    }

//    public Person(String name) {
//        this.name = name;
//    }

    public static void main(String[] args) {
        Person person = new Person("jack");
        System.out.println(person.name);
    }
}


/*
 * ============================================================
 * 构造函数（构造方法）笔记
 * ============================================================
 *
 * 基本概念：
 *   用来初始化对象的方法；
 *   在对象创建时就能初始化部分字段，
 *   而无需等到对象创建之后。
 *
 * ============================================================
 * 核心规则
 * ============================================================
 *
 * 1. 构造函数是一个对象初始化的过程；
 *
 * 2. 一个类没有构造函数时，默认会有一个无参构造函数；
 *
 * 3. 如果一个类里编写了构造函数，那么默认的无参构造函数会被移除；
 *
 * 4. 构造函数的名称必须和类名一样，且不能有返回值；
 *
 * 5. 一个类可以有多个构造函数，只要入参不同即可（构造方法重载）。
 *
 */

