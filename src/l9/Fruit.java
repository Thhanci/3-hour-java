/*
    Homework:




    ============================================================
    static 修饰方法（静态方法）
    ============================================================

    【什么是静态方法】
    用 static 修饰的方法，属于类本身，不属于任何对象。
    直接用 类名.方法名() 调用，不需要创建对象。

    【特点】
    1. 无需创建对象，直接用类名调用
    2. 不能直接访问实例变量和实例方法
    3. 只能直接访问静态变量和静态方法
    4. 所有对象共用同一个静态方法

    【适用场景】
    1. 工具类方法（如 Math.max()、Integer.parseInt()）
    2. 工厂方法（如 Integer.valueOf()）
    3. 程序入口（main 方法必须是 static）
    4. 不需要对象状态，只依赖传入参数的方法

    【代码示例】
    public class Calculator {
        static String version = "1.0";   // 静态变量
        String brand = "Casio";          // 实例变量

        // 静态方法
        public static int add(int a, int b) {
            // ✅ 可以访问静态变量
            System.out.println("版本：" + version);
            // ❌ 不能访问实例变量
            // System.out.println(brand);  // 编译报错
            return a + b;
        }

        // 实例方法
        public int multiply(int a, int b) {
            // ✅ 可以访问静态变量
            System.out.println("版本：" + version);
            // ✅ 可以访问实例变量
            System.out.println("品牌：" + brand);
            return a * b;
        }

        public static void main(String[] args) {
            // ✅ 静态方法：类名直接调用
            int sum = Calculator.add(3, 5);
            System.out.println(sum);  // 8

            // ❌ 实例方法：必须先创建对象
            Calculator calc = new Calculator();
            int product = calc.multiply(3, 5);
            System.out.println(product); // 15
        }
    }

    【对比总结】
    对比项          静态方法（static）      实例方法（非static）
    ─────────────────────────────────────────────────
    属于           类本身                  对象
    调用方式       类名.方法名()           对象.方法名()
    能否访问实例变量  ❌ 不能                ✅ 能
    能否访问静态变量  ✅ 能                  ✅ 能
    适用场景       工具类方法             操作对象特有数据的方法

    ============================================================



*/