/*

    ============================================================
    变量作用域 — 基本类型
    ============================================================

        局部变量
            方法中或者某局部块中声明定义的变量或方法的参数。

        实例变量
            从属于对象（也即实例）。各对象间的实例变量互不干扰，
            能通过实例的引用来访问实例变量。

        类变量（静态变量）
            用 static 关键字修饰，从属于类。
            所有由一个类生成的对象都共用这个类的类变量。

    ============================================================







    ============================================================
    变量作用域 — 比较
    ============================================================

        对比项            局部变量              实例变量              类变量
        ─────────────────────────────────────────────────────────────────────────────
        定义位置          方法中，或方法         在类中，方法外         在类中，方法外
                          的形式参数

        默认值            无默认值，必须先       有默认值              有默认值
                          赋值后才能使用

        调用方式          /                     对象调用              类调用、对象调用

        生命周期          与方法相同             与对象相同            与类相同

        别名              /                     /                     静态变量

    ============================================================






    ============================================================
    变量作用域 — 区分的价值
    ============================================================

        局部变量
            帮助程序员更好地编码，节省能够节省的空间。

        实例变量
            平时使用最多的一种变量，能够体现面向对象的优势。

        类变量（静态变量）
            多实例共享的变量，例如共享的程序配置信息等等。

    ============================================================

*/


/*

    ============================================================
    三类变量对比
    ============================================================

    变量类型                            定义位置         调用方式            特点
    ─────────────────────────────────────────────────────────────
    局部变量function(int _a){int b=0;}  方法内部          直接用名字       方法结束就销毁
    实例变量int a;                      类内部，方法外     对象.变量名      每个对象各有一份
    类变量static a;                     类内部，加       类名.变量名       所有对象共享一份


    ============================================================


public class Person {
    // 类变量（静态变量）：属于类，所有对象共享
    static String country = "中国";

    // 实例变量：属于对象，每个对象独立
    String name;
    int age;

    public void show(String message) {  // message 是局部变量（方法参数）
        // 局部变量：定义在方法内部
        String temp = "信息：";          // temp 是局部变量

        System.out.println(temp + name + "，" + age + "，" + message);
    }

    public static void main(String[] args) {
        // 1. 类变量直接用类名调用
        System.out.println(Person.country);  // 中国

        // 2. 实例变量必须通过对象调用
        Person p1 = new Person();
        Person p2 = new Person();

        p1.name = "张三";
        p1.age = 18;
        p2.name = "李四";
        p2.age = 20;

        // 每个对象有自己的 name 和 age，互不影响
        System.out.println(p1.name + "，" + p1.age);  // 张三，18
        System.out.println(p2.name + "，" + p2.age);  // 李四，20

        // 3. 类变量所有对象共享，改一个全变
        Person.country = "美国";
        System.out.println(p1.country);  // 美国
        System.out.println(p2.country);  // 美国
    }
}

*/


