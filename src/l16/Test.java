/*

    ============================================================
    static 关键字
    ============================================================

    【用法】

    static 可以修饰变量、方法、类、局部块。


    【访问方式】

    - 可以通过类或者对象进行访问；
    - 推荐通过类进行访问。


    【加载时间】

    static 块在类第一次被加载时加载；
    程序运行时只会调用一次；
    运行顺序：static 块 → 构造函数


    【代码示例】

    public class Person {
        // 静态块：类加载时执行（只执行一次）
        static {
            System.out.println("1. static 块执行");
        }

        // 构造方法：每次 new 对象时执行
        public Person() {
            System.out.println("2. 构造方法执行");
        }

        public static void main(String[] args) {
            Person p1 = new Person();   // static 块已先执行，再执行构造
            Person p2 = new Person();   // static 块不再执行，只执行构造
        }
    }


    【执行顺序】

    程序启动 → 类加载 → static 块执行（1次） → main 方法执行 → new 对象 → 构造方法执行（每次 new 都执行）


    【对比总结】

    对比项              static 块                         构造方法
    ──────────────────────────────────────────────────────────────────
    执行时机           类加载时（早）                     new 对象时（晚）
    执行次数           只执行 1 次                       每次 new 都执行
    谁触发             JVM 类加载器自动触发               new 关键字触发
    用途               初始化静态变量、加载配置文件        初始化对象实例
    生活类比           开店前的设备安装                   每生产一件产品的组装流程

    ============================================================

*/

/*

    ============================================================
    static 块常见误区
    ============================================================

    【问题】
    静态块能否在 for 循环里调用？

    【答案】
    不能。静态块只能写在类体中，不能写在任何方法或代码块内部。


    【错误示例】

    public class Test {
        public static void main(String[] args) {
            for (int i = 0; i < 3; i++) {
                static {   // ❌ 编译报错：静态块不能放在方法里
                    System.out.println("static 块执行");
                }
            }
        }
    }


    【正确做法】

    如果想让静态块里的逻辑执行多次：
    把逻辑写在普通方法里，在循环中调用该方法。

    public class Test {
        // 静态块：类加载时自动执行一次
        static {
            System.out.println("1. static 块执行（只一次）");
        }

        // 普通方法：可以被多次调用
        static void doSomething() {
            System.out.println("2. 方法执行");
        }

        public static void main(String[] args) {
            for (int i = 0; i < 3; i++) {
                doSomething();   // ✅ 每次循环都执行
            }
        }
    }


    【执行结果】

    1. static 块执行（只一次）
    2. 方法执行
    2. 方法执行
    2. 方法执行


    【核心结论】

    - 静态块不能主动调用，只能由类加载器自动执行；
    - 静态块不能放在方法或循环里；
    - 想让代码执行多次，用普通方法 + 循环调用。

    ============================================================

*/